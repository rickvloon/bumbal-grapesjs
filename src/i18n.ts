// Allows the nl/fr/de resources above to only specify what they actually
// have (they all cover every key in practice, but this keeps TS from
// demanding an exhaustive mirror of `en`'s shape at the type level).
type RecursivePartial<T> = { [P in keyof T]?: T[P] extends object ? RecursivePartial<T[P]> : string };

export const supportedLocales = ["en", "nl", "fr", "de"] as const;
export type SupportedLocale = (typeof supportedLocales)[number];

let instance: i18n | undefined;

// Lazily initialized (rather than at module load) so multiple editor
// instances on the same page - or a page that never sets a `locale` at all -
// don't all fight over creating/registering the global i18next singleton.
export const initI18n = (locale: string): i18n => {
	if (!instance) {
		instance = i18next.createInstance();
		instance.init({
			lng: locale,
			fallbackLng: "en",
			resources: {
				en: { translation: en },
				nl: { translation: nl },
				fr: { translation: fr },
				de: { translation: de },
			},
			interpolation: { escapeValue: false },
		});
	} else if (instance.language !== locale) {
		instance.changeLanguage(locale);
	}

	return instance;
};

export const t = (key: string, options?: Record<string, unknown>): string => {
	if (!instance) return key;
	return instance.t(key, options) as string;
};
